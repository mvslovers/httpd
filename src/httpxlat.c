/* HTTPXLAT.C
** ASCII/EBCDIC translation tables and codepage management.
**
** Three codepage options:
**   CP037    — IBM CECP for US/Canada (symmetric NL/LF, default)
**   IBM1047  — IBM Open Systems Latin-1 (used by z/OS USS, Zowe)
**   LEGACY   — HTTPD 3.3.x hybrid tables (backward compatibility)
**
** The server default is selected via Parmlib CODEPAGE parameter.
** CGI modules can choose a specific codepage per call via the
** HTTPX vector table.
*/
